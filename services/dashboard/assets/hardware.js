import getHeaders from '../../../libs/utils/getHeaders';
import axiosInstance from '../../config';

export async function getHardwareAssetsService(accessToken, apiPath) {
  const headers = getHeaders(accessToken);
  try {
    // const path = page ? `/admin/hardware-assets/?page=${page}` : '/admin/hardware-assets/';
    const data = await axiosInstance.get(apiPath , { headers });
    return { data };
  } catch (error) {
    const { response: { data, status, statusText } } = error;
    let errors = {};
    if (status === 400 && data && Object.keys(data).length) {
      errors = data;
    } else {
      errors.nonFieldErrors = [statusText, 'Please try later'];
    }
    return { errors };
  }
}

